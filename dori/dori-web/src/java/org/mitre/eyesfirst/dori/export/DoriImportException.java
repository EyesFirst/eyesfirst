package org.mitre.eyesfirst.dori.export;

public class DoriImportException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = -6742343973250374694L;

	public DoriImportException() {
		super();
	}

	public DoriImportException(String message, Throwable cause) {
		super(message, cause);
	}

	public DoriImportException(String message) {
		super(message);
	}

	public DoriImportException(Throwable cause) {
		super(cause);
	}

}
